+(CCScene *) scene
{
	// 'scene' is an autorelease object.
	CCScene *scene = [CCScene node];
	
	// 'layer' is an autorelease object.
	HelloWorldLayer *layer = [HelloWorldLayer node];
	
	// add layer as a child to scene
	[scene addChild: layer];
	
	// return the scene
	return scene;
}

-(id) init
{
	if( (self=[super init])) {
		
		// enable events
		
		self.isTouchEnabled = YES;
		self.isAccelerometerEnabled = YES;
		CGSize s = [CCDirector sharedDirector].winSize;
		
		// init physics
		[self initPhysics];
        
        _levelHelperLoader = [[LevelHelperLoader alloc]initWithContentOfFile:@"testlevel1"];
        
		[_levelHelperLoader addObjectsToWorld:world cocos2dLayer:self];
        [_levelHelperLoader createGravity:world];
        
        if ([_levelHelperLoader hasPhysicBoundaries])
        {
            [_levelHelperLoader createPhysicBoundaries:world];
        }
        

        [self scheduleUpdate];
	}
	return self;
}

-(void) dealloc
{
    [_levelHelperLoader release];
    _levelHelperLoader = nil;
    
	delete world;
	world = NULL;
	
	delete m_debugDraw;
	m_debugDraw = NULL;
	
	[super dealloc];
}	

